// Operator Aritmatika
let hargaA = 10000;
let hargaB = 5000;
let totalHarga = hargaA + hargaB; // 15000
console.log("Total harga:", totalHarga);

let sisaUang = 20000 % 7000; // 6000
console.log("Sisa uang:", sisaUang);
